// Creating Object

let student = {
    name: "Goutham",
    age: 20,
    course: "CSE"
};

// Print whole object

console.log(student)

// Access Object Properties

console.log(student.name)
console.log(student.age)

// Update Property

student.age = 21
console.log(student)

// Add New Property

student.city = "Bangalore"
console.log(student)
